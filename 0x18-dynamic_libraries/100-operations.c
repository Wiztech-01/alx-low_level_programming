#include<stdio.h>

/**
 * add - adds two integers and returns the result
 * @a: first integer
 * @b: second integer
 *
 * Return sum of a and b
 */
int add(int a, int b)
{
	return (a+b);
}

int sub(int a, int b)
{
	return (a-b);
}

int mul(int a, int b)
{
	return (a*b);
}

int div(int a, int b)
{
	return (a/b);
}

int mod(int a, int b)
{
	return (a%b);
}

